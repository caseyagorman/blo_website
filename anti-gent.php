				<h2>BLO's
Commitment to Resisting Gentrification</h2>

<p>
One of the highest political priorities for the BLO is fighting the displacement caused by gentrification. We believe that everyone has a right to a stable home and that low-cost housing and small businesses should be protected from the boom and bust cycles of the real estate market.
</p>

<h3>
What is Gentrification?
</h3>

<p>
<blockquote>
gen&middot;tri&middot;fi&middot;ca&middot;tion
&ndash;noun 1. the buying and renovation of houses and stores in deteriorated urban neighborhoods by upper- or middle-income families or individuals, thus improving property values but often displacing low-income families and small businesses.
<cite>Random House Unabridged Dictionary, &copy; Random House, Inc. 2006.</cite>
</blockquote>
</p>

<p>
Neighborhoods change over time. Whenever an area is becoming &ldquo;more desirable,&rdquo; for whatever reason, property values begin to rise. At first, this just means that there are fewer vacant apartments and other spaces. Although not mentioned in the definition above, this too is part of gentrification, since with rising property values and fewer vacancies, the buying and renovations described above soon follow.
</p>

<p>
This &ldquo;first wave&rdquo; of gentrification is almost always made up of low-to-middle-income artists: people just like the majority of us in the band. Almost all of us in the BLO are implicated in gentrification because we rent our homes in low-income neighborhoods like West Oakland, East Lake Merritt, and San Francisco&rsquo;s Mission District. None of us are natives of these areas&mdash;we&rsquo;ve all moved in within the last 10 years. 
</p>

<p>
Furthermore, gentrification in the United States nearly always has a racial component, since race and class correlate so closely in this country. It&rsquo;s no coincidence that the above neighborhoods have, for generations, been home to working-class African-Americans and/or immigrants. Thus, part of the dynamic of gentrification dovetails with our culture&rsquo;s ever-prevalent racism: once artists begin renting in a poorer neighborhood, the very fact that many of us are white means that other, richer white people can look around and &ldquo;feel safer.&rdquo; [Of course, not all artists (or BLO members!) are white. Overall however, the broad class of society that chooses lifestyles such as "artist," 
"activist," etc., tend to come from the middle class. Because of the close correlation of race and class in the U.S., the overwhelming majority of these groups are white.] The &ldquo;second wave&rdquo; of gentrification tends to be white-collar professionals. These eventually displace the first wave too: property values and rents rise further until the artists themselves are in turn displaced. 
</p>

<p>
Some people argue that gentrification is merely economic development: a neutral process or even good for a neighborhood. We believe, however, that economic development is only positive to the extent that it empowers and uplifts those already living in the neighborhood.  If development means that families can no longer afford to live in their newly-developed neighborhood, or local shopkeepers can't afford their rents, then this is development at the cost of human lives.  Economic development should be based on the real needs and input of people living (not only owning) in a neighborhood, and not on the schemes of developers whose only concern is profit.
</p>




<h3>
What can be done about gentrification?
</h3>

<p>
Gentrification is a complex process&mdash;many factors contribute to neighborhood change. Within our group and among our political allies, there is much discussion and debate about the relative importance of various factors and various specific cases. 
</p>

<p>
How much impact does a particular new, higher-end store or restaurant have on a given city block?  Does renting or buying a home on a given street change that area too much? These can be difficult questions. Fortunately, there are concrete ways to minimize the most egregious aspects of gentrification. Low-income residents and advocates of affordable housing and locally-owned businesses have formed organizations and developed strategies that include the following:
</p>

<p>
<ul>
<li>Rent Control and tenant protections.</li>
<li>Planning and land-use policy that prioritizes low-income housing over market-rate development.
<li>Stopping certain development projects that will certainly exacerbate gentrification (especially market-rate condominiums.)</li>
<li>Fighting specific evictions of long-term tenants.</li>
<li>Programs such as &ldquo;land trusts&rdquo; that permanently halt market speculation on specific properties</li>
<li>Programs that help people gain equity in the homes and communities where they live.</li>
</ul>
</p>
 
