				<h3>- Music is liberation:</h3>
				<p>Culture is a celebration of life and human creativity. We use music and artistic expression as a response to oppressive society, to sustain and build our movements, and as an expression of the world that we want to live in. BLO is a group of musicians (of all levels) and cultural workers who use culture to support causes of a broadly left nature. </p>

				<h3>- Racial and Social Justice:</h3> 
				<p>We work to challenge and eliminate all forms of domination (racism, class oppression, sexism, hetero-sexism, ageism, abilism, etc.) both within our group and within the broader society. We pay particular attention to racism and white supremacy as we see these forms of oppression as a primary obstacle to building the just society that we all want to live in.</p>

				<h3>- Diversity of political strategies:</h3>
				<p>We agree that we will work together, making political art to contribute to leftist struggles without promoting any ideological tendency on the Left over another. We work in the spirit of Left unity to overcome the fragmentation of the Left in the United States.</p> 

				<h3>- Respect for Culture:</h3>
				<p>We work for a society that respects all cultures and work to promote cross-cultural understanding, social justice and human solidarity. We attempt to do this in a manner that avoids exploitation, stealing or ignorance of the world's cultures.</p>

				<h3>- Respect for the Earth:</h3>
				<p>We work to reverse the trend of domination and misuse of the world's natural resources. Many of us are against capitalism because the destruction of the world's environment has reached the level of ecocide. </p>


