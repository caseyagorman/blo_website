<h3><a name="outline">Outline of the action</a></h3>
(of course it never goes quite like this in real life...)
<img style="float:right;" src="images/healthcareSigns.jpg" alt="Pro-healthcare protesters at Whole Foods" width="370" height="321" border="0">
<p>
Overall tone:  Fun, cheerfully informative.  we comply with most instructions with a smile (i.e. "leave the store now") but do so
gradually.  Avoid hostile engagement. 
</p>

<p>
<h4>SCOUT LOCATION</h4>
Figure out best location in the store, how we can do it without pissing off the employees too much. 
</p>

<p>
<h4>REHEARSAL AND RUN THROUGH</h4>
<b>4:45</b> arrive nearby park for rehersal
</p>

<p>
Roles:
<ul>
	<li> 5+ several musicians</li>
	<li> 5+ sing/shout/dancers</li>
	<li> 3+ videographers</li>
	<li> 3+  people with concealed signs, <a href="images/whole foods flyer.pdf">flyers</a></li>
	<li> 2+ "manager liaison"  (some one who will step up to talk with managers/security to delay them)</li>
</ul>

Everyone in ordinary street clothes, perhaps with cloth shopping bangs to conceal <a href="images/whole foods flyer.pdf">fliers</a> etc. Nothing overtly "activisty"
</p>

<p>
<b>5:45</b>  People start leaving park to go to whole foods (6 min walk)
</p>

<p>
<h4>MAIN ACTION</h4>

<b>6:00</b>  we start entering whole foods one by one and begin shopping. Musicians with instruments in cases (i.e. you are clearly a musician, but musicians shop for for groceries also).  Bass drum and sousaphone arrive last, remain outside.  Video people find their locations
</p>
<img style="float:right;" src="images/boss_sick.jpg" alt="Is the boss sick? Sign from pro-healtcare protest of John Mackey CEO of Whole Foods" width="287" height="370" border="0">
<p>
<b>6:09</b> video people pull out cameras (may get hassled by security, that's why we have more than one) musicians (in aisles) start putting instruments together
dancers start converging to the space infront of the in-store "Cafe Gratitude"
</p>

<p>
<b>6:10 </b>  3 dancers start singing "hey macky".  After first chorus, more dancers put down their groceries and join in.   After 1st time through, musicians step out of aisles as they are ready and join in.  Sousaphone and bass drum enter through front of store and join in.  Choreography
starts.   If manager or security  tries to interrupt, manger liaison tries to gently intercept
</p>


<p>
<b>6:17 </b> After 2nd time through, everyone stops.  someone announces:

"John Mackey, the CEO of Whole Foods has argued that American workers do not deserve a health care system.  We believe that heath care should be
affordable for everyone, not just rich people.  So Mr. Mackey, this song is for you!" (or words to that effect)  People who have signs reveal them.
</p>

<p>
<b>6:18</b> Music and song resume.  People hand out <a href="images/whole foods flyer.pdf">flyers</a>.  Everyone starts
dancing gradually towards the exits. We did a few more choruses and another announcement.
</p>

<p>
<b>6:20</b> everyone out the front door, stop playing.  If people are not
upset, we may play a few songs and flyer on the corner.  Otherwise we move
efficiently off Whole Foods property, especially video people.
</p>

<p>
<b>9:00</b>  Edit video, post to everyone, possibly news outlets? 
</p>


