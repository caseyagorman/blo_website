<h3>Why did we do it?</h3>
This action was in response to a recent Op-Ed in the Wall Street Journal by Whole Foods CEO John Mackey in which 
<a href="http://online.wsj.com/article/SB10001424052970204251404574342170072865070.html#articleTabs%3Darticle">he argued healthcare is not an intrinsic right</a>. 

<br><br>
We believe that everyone should have access to healthcare, and that the U.S. should have an affordable public insurance option -- one that assists sick people rather than insurance companies.  Mackey's statements seem to indicate that he is out of touch with the reality faced by most working people today and his positions seem contrary to the "sustainable lifestyle" imagery the store promotes. 

<br><br>So, at a prearranged time and signal, we convened in the store and launched into singing: 
<br>"Hey Mackey, you're a swine!"
<br><br>Some of us danced while others passed out <a href="images/whole foods flyer.pdf">informational flyers</a>...and the B.L.O. accompanied with an adaptation of Toni Basil's "Hey Mickey" and added to the commotion! We had a blast, and even got <a href= "#media">some press coverage for the stunt</a>. The customers and staff seemed mostly amused, several said they were unaware of Mackey's position, and a few even vowed not to shop there anymore.    

